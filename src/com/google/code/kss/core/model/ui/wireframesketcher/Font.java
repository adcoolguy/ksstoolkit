package com.google.code.kss.core.model.ui.wireframesketcher;

import org.simpleframework.xml.Attribute;

public class Font {

	@Attribute(required=false)
	private int size;

	public int getSize() {
		return size;
	}

	@Attribute(required=false)
	private boolean bold;

	public boolean isBold() {
		return bold;
	}

}
